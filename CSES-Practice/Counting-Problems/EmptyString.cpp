ll choose[260][260];
void ncr_compute() {
    for(ll i=0; i<260; i++) {
        choose[i][0]=1;
        for(ll j=1; j<=i; j++) {
            choose[i][j] = (choose[i-1][j]+choose[i-1][j-1])%MOD;
        }
    }
}

void solve() {
    // ll n; cin>>n;
    string s; cin>>s;
    ll n=s.length();
    vvl dp(n+1, vl(n+1,0));
    for(ll i=0; i<n; i++)  dp[i+1][i]=1;

    for(ll i=n-1; i>=0; i--) {
        for(ll j=i+1; j<n; j+=2) {
            for(ll k=i+1; k<=j; k+=2) {
                if(s[i]==s[k]) {
                    ll temp=(1LL* dp[i+1][k-1]* dp[k+1][j])%MOD;
                    ll temp2 = (1LL* temp* choose[(j-i+1)/2][(k-i+1)/2])%MOD;
                    dp[i][j]=(dp[i][j]+temp2)%MOD;
                }
            }
        }
    }

    cout<<dp[0][n-1];
}
 
 
